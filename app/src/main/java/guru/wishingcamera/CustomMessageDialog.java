package guru.wishingcamera;


import android.app.AlertDialog;
import android.app.Dialog;
import android.content.Context;
import android.content.DialogInterface;
import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.support.v7.app.AppCompatDialogFragment;
import android.view.LayoutInflater;
import android.widget.EditText;


/**
 * A simple {@link Fragment} subclass.
 * This is a dialog for the custom message
 */
public class CustomMessageDialog extends AppCompatDialogFragment {

    private CustomMessageDialogListener customMessageListener=null;

    public interface CustomMessageDialogListener {
        public void onCustomMessageDialogOk(DialogInterface dialog, String message);
    }


    public CustomMessageDialog() {
        // Required empty public constructor
    }

    @Override
    public Dialog onCreateDialog(Bundle savedInstanceState) {
        AlertDialog.Builder builder = new AlertDialog.Builder(getActivity());
        // Get the layout inflater
        LayoutInflater inflater = getActivity().getLayoutInflater();

        // Inflate and set the layout for the dialog
        // Pass null as the parent view because its going in the dialog layout
        builder.setView(inflater.inflate(R.layout.fragment_custom_message_dialog, null))
                // Add action buttons
                .setPositiveButton(R.string.ok, new DialogInterface.OnClickListener() {
                    @Override
                    public void onClick(DialogInterface dialog, int id) {
                        EditText customMessageEdit = (EditText) CustomMessageDialog.this.getDialog().findViewById(R.id.custom_message);
                        customMessageListener.onCustomMessageDialogOk(dialog, customMessageEdit.getText().toString() );
                    }
                })
                .setNegativeButton(R.string.cancel, new DialogInterface.OnClickListener() {
                    public void onClick(DialogInterface dialog, int id) {
                        CustomMessageDialog.this.getDialog().cancel();
                    }
                });
        return builder.create();
    }

    // Override the Fragment.onAttach() method to instantiate the NoticeDialogListener
    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        // Verify that the host activity implements the callback interface
        try {
            // Instantiate the NoticeDialogListener so we can send events to the host
            customMessageListener = (CustomMessageDialog.CustomMessageDialogListener) context;
        } catch (ClassCastException e) {
            // The activity doesn't implement the interface, throw exception
            throw new ClassCastException(context.toString()
                    + " must implement CustomMessageDialogListener");
        }
    }
}
